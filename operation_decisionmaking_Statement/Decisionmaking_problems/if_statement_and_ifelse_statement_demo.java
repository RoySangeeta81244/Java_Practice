package operation_decisionmaking_Statement.Decisionmaking_problems;
// if statement
public class if_statement_and_ifelse_statement_demo {
    public static void main(String[] args) {
        boolean isRaining = true;
        if (isRaining==true) {
            System.out.println("I will go Outside");
        }



        //if-else statement
        boolean itRaining = false;
        if (itRaining==true) {
            System.out.println("I will go Outside");
        }
        else{
            System.out.println("I will stay at Home");
        }
    }
}
