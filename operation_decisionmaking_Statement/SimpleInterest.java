package operation_decisionmaking_Statement;
//Java Program to calculate Simple Interest
public class SimpleInterest {
    public static void main(String[] args) {
        int P = 5000; //Here P denotes the Principle Amount
        int R = 10; // Here R denotes the Rate of Interest
        int T = 5; // Here T denotes the time
        int SI = P*R*T/100; // Here SI denotes the Simple Interest
        System.out.println("The Simple Interest is "+SI);
    }
}
