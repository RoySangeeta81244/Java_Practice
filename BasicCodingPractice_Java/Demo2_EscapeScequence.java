package BasicCodingPractice_Java;

public class Demo2_EscapeScequence {
    public static void main(String[] args) {
        System.out.println("Sangeeta Roy \nFemale \nSoftware Test Engineer\n");
        // by using \n -> we get a new line
        System.out.println("1 \t 2 \n3 \t 4\n5 \t 6\n");
        //by using \t -> we get a tab
        System.out.println("\'Software Test Engineer\'");
        System.out.println("\"Software Test Engineer\"");
        //by using \' -> we get a single quote
        //by using \" -> we get a double quote
    }

    
}
